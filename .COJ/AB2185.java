import java.util.Scanner;


public class AB2185 {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int n = in.nextInt();
		int n2 = in.nextInt();
		System.out.println(n+n2);
		System.out.println(n-n2);
		System.out.println(n*n2);
		System.out.println(n/n2);
		System.out.println(n%n2);

	}

}
