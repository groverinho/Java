import java.util.Scanner;


public class RestaYmultiplicacion {

	public static void main(String[] args)
	{
		Scanner in = new Scanner(System.in);
		int A = in.nextInt();
		int B = in.nextInt();
		int C = in.nextInt();
		int D = in.nextInt();
		int result = (A-B)*(C-D);
		System.out.println(result);

	}

}
